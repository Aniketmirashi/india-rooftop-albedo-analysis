def h3_to_polygon(cell):
    """
    Convert an H3 cell to a Shapely Polygon.
    """

    boundary = h3.cell_to_boundary(cell)

    return Polygon(
        [(lng, lat) for lat, lng in boundary]
    )


def aggregate_city_to_h3(city_gdf, resolution):
    """
    Aggregate rooftop observations into H3 hexagons.
    """

    data = city_gdf.copy()

    # Assign H3 Index
    data["H3_Cell"] = data.apply(
        lambda row: h3.latlng_to_cell(
            row.lat,
            row.lng,
            resolution
        ),
        axis=1
    )

    # Aggregate Statistics
    hex_df = (
        data
        .groupby("H3_Cell")
        .agg(
            Mean_Albedo=("mean_albedo", "mean"),
            Median_Albedo=("mean_albedo", "median"),
            Std_Albedo=("mean_albedo", "std"),
            Roof_Count=("mean_albedo", "count")
        )
        .reset_index()
    )

    # Convert H3 to Polygon
    hex_df["geometry"] = hex_df["H3_Cell"].apply(
        h3_to_polygon
    )

    return gpd.GeoDataFrame(
        hex_df,
        geometry="geometry",
        crs=CONFIG["CRS"]
    )

