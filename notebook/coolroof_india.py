def clip_rooftop_points(roof_gdf, city_boundaries_gdf):
    """
    Clip rooftop observations to administrative city boundaries.

    Parameters
    ----------
    roof_gdf : GeoDataFrame
        Google rooftop observations.

    city_boundaries_gdf : GeoDataFrame
        Administrative city boundaries.

    Returns
    -------
    city_points : dict
        Dictionary containing clipped GeoDataFrames.

    clipping_summary_df : pandas.DataFrame
        Summary statistics for clipping operation.
    """

    city_points = {}

    clipping_summary = []

    for _, row in city_boundaries_gdf.iterrows():

        city = row["City"]

        print(f"Processing {city}...")

        boundary = gpd.GeoDataFrame(
            [row],
            crs=city_boundaries_gdf.crs
        )

        clipped = gpd.clip(
            roof_gdf,
            boundary
        )

        city_points[city] = clipped

        clipping_summary.append({

            "City": city,

            "Roof_Observations": len(clipped),

            "Mean_Albedo": clipped["mean_albedo"].mean(),

            "Minimum_Albedo": clipped["mean_albedo"].min(),

            "Maximum_Albedo": clipped["mean_albedo"].max()

        })

    clipping_summary_df = pd.DataFrame(
        clipping_summary
    )

    return city_points, clipping_summary_df

